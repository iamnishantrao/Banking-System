    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
                
        Statement st = new DBConnect().getStatement();
        
        try {
            ResultSet rs = st.executeQuery("select * from customer where user_name = '"+user+"'");
            rs.next();
            String acc_no = rs.getString("acc_no");
            System.out.println(acc_no);
            
            st.executeUpdate("update customer set c_name = '"+jTextField1.getText()+"', c_add = '"+jTextField2.getText()+"', age = '"+jTextField3.getText()+"', contact_no = '"+jTextField4.getText()+"', email = '"+jTextField5.getText()+"' where acc_no = '"+acc_no+"'");
        } catch (Exception e) {
            System.out.println(e);
        }
        
        this.dispose();
        JOptionPane.showMessageDialog(null, "Your Account has been updated.");
        new UserPage(user).setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

