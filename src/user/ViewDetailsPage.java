    public ViewDetailsPage(String user) {
        this.user = user;
        initComponents();
        
        Statement st = new DBConnect().getStatement();
            try
            {
                ResultSet rs = st.executeQuery("select * from customer where user_name = '"+user+"'");
                rs.next();
                if(rs != null)
                {
                    jTextField1.setText(rs.getString("c_id"));
                    jTextField2.setText(rs.getString("acc_no"));
                    jTextField3.setText(rs.getString("acc_type"));
                    jTextField4.setText(rs.getString("c_name"));
                    jTextField5.setText(rs.getString("c_add"));
                    jTextField6.setText(rs.getString("age"));
                    jTextField7.setText(rs.getString("contact_no"));
                    jTextField8.setText(rs.getString("email"));
                    jTextField9.setText(rs.getString("gender"));
                    jTextField10.setText(rs.getString("user_name"));
                }
            }catch(Exception e)
            {
                System.out.println(e);
            }
    }

